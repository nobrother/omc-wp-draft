<script>
	(function($, global, state){

		var hashChanged = function(e){
			var hash = global.location.hash,
					el = $(hash);
			return (el.length && el.goTo() && e.preventDefault());
		}
		$(function(){
			$(global).on('load statechange', hashChanged)
			$('body').on('click', '[data-goto]', function(e){
				var selector = $(this).data('goto'),
						el = $(selector);
				if(el.length){
					e.preventDefault();
					el.goTo('fast', function(){
						state && state.pushState({}, '', selector);
					});
				}
			})
		});
	})(jQuery, window, history)
</script>
